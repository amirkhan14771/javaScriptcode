/*
A process is an instance of a program that is being executed by one or many
threads.A thread is a lightweight, independent unit of execution that exists
within a process.Each thread has its own program counter, stack, and local
variables.Threads within the same process share the same memory space and can
communicate with each other easily, while threads in different processes have
separate memory spaces and must use inter - process communication(IPC)
mechanisms to communicate.In short, a process is a program in execution,
 while a thread is a single execution sequence within that process. */

